let userConfig = undefined

/** @type {import('next').NextConfig} */
const nextConfig = {
  eslint: {
    ignoreDuringBuilds: true,
  },
  typescript: {
    ignoreBuildErrors: true,
  },
  images: {
    unoptimized: true,
  },
  experimental: {
    webpackBuildWorker: true,
    parallelServerBuildTraces: true,
    parallelServerCompiles: true,
  },
}

mergeConfig(nextConfig, userConfig)

function mergeConfig(nextConfig: { [x: string]: any; eslint?: { ignoreDuringBuilds: boolean }; typescript?: { ignoreBuildErrors: boolean }; images?: { unoptimized: boolean }; experimental?: { webpackBuildWorker: boolean; parallelServerBuildTraces: boolean; parallelServerCompiles: boolean } }, userConfig: { [x: string]: any } | undefined) {
  if (!userConfig) {
    return
  }

  for (const key in userConfig) {
    if (
      typeof nextConfig[key] === 'object' &&
      !Array.isArray(nextConfig[key])
    ) {
      nextConfig[key] = {
        ...nextConfig[key],
        ...userConfig[key],
      }
    } else {
      nextConfig[key] = userConfig[key]
    }
  }
}

export default nextConfig